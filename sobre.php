    <p class='first_text'>
        A Nossa escola foi criada pelo decreto nº 4.227, de 1º de junho de 1954.
        Sua instalação ocorreu em 19 de março de 1965, com o nome de Grupo Escolar
        “Professor Juvenal Brandão”. O Patrono escolhido é o Professor Juvenal Sanches
        de Lemos Brandão, que sempre se dedicou às causas relacionadas à Educação.
        A escola iniciou seu funcionamento em um prédio alugado, à Rua General Osório, nº 64.
        Com o esforço conjunto da diretoria e comunidade escolar, finalmente, no dia 19 de março
        de 1972, inaugurou-se o novo prédio com o nome de “E.E. Professor Juvenal Brandão”, onde
        funciona até os dias de hoje, à Rua Engenheiro Américo Guide Filho, nº 48.
    </p>

    <h3 style='margin-left: 20px;'> Juvenal Sanches de Lemos Brandão</h3>

    <p class='first_text'>
        Desde criança, manifestou desejo de estudar e como residia com seus pais na Fazenda Bom Viver, vem para a zona urbana morar com seus parentes, no Solar de Cel Paiva, onde permaneceu, convivendo com seu primo, Julio Bueno Brandão, com o qual concluiu o curso primário, com o velho professor João Carlos Schimidt.
        Ao terminar seu curso de humanidades no Colégio da cidade de Campo Belo, matriculou-se na Faculdade de Medicina no Rio de Janeiro, no entanto uma epidemia de febre amarela, o fez regressar à Ouro Fino novamente, onde lecionou Ciências em um colégio particular.
        Seu ideal em se formar em uma escola superior o impulsiona a migrar para São Paulo e matricular-se na faculdade de Direito. Porém com a morte de seu pai, teve que assumir a Fazenda da família e deixar, por hora, seu sonho de lado.
    </p>

