    <p class='first_text'>
        Na cidade, Juvenal se dedica ao magistério, enfrentando todas as dificuldades, funda um colégio em Ouro Fino, denominado, Instituto Municipal, talvez um dos primeiros do Estado de Minas Gerais.
        Com o passar do tempo começou a lecionar Matemática e Línguas em outras escolas. Nesta época conheceu sua esposa, Etelvina. Só deixou a sala de aula quando foi nomeado pelo governador do estado, Inspetor Técnico de Ensino.
        Essa nomeação fez com que o Senhor Juvenal viajasse constantemente pelo Sul de Minas, onde fiscalizava e normalizava o ensino. Enfrentando divergências, mas nuca desistindo, conseguiu solucionar muitas divergências, demonstrando características diplomáticas. Durante essas viagens ele chegou à conclusão de que muitas professoras não tinham formação pedagógica, assim havendo uma necessidade urgente de adaptação dos programas de ensino. Essa ideia foi discutida em Congressos e à partir daí foram criadas as “Escolas Normais”.
    </p>

