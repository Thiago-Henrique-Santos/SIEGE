    <p class='first_text'>
        O professor Juvenal Brandão era um homem de visão e comprometido com sua época para a expansão e atualização do ensino em Minas Gerais. Previu a necessidade de auxílio financeiro aos alunos sem recursos suficientes para se manterem na escola, à princípio com as Caixas-Escolares.
        Ao longo dos anos colaborou com revistas de Ensino, debatendo soluções de problemas educacionais, sendo também diretor da E. E. Coronel Paiva.
        Por sua dedicação à causa de ensino e como exemplo de educador íntegro e um verdadeiro idealista, foi escolhido para patrono de nossa querida escola.
